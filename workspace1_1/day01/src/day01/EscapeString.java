package day01;

public class EscapeString {
	public static void main(String[] args) {
		// 제어문자
		// \n 
		System.out.println("안녕\n하세요");
		// \t
		System.out.println("안녕\t하세요");
		// \"
		System.out.println("\"안녕\"하세요");
		// \'
		System.out.println("\'안녕\'하세요");
		// \\
		System.out.println("제어문자는 \\로 시작합니다.");
		
	}

}
